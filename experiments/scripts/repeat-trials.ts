/**
 * Repeated Trials Experiment
 *
 * Runs each model x prompt x condition 5 times to get statistical significance.
 * Reports mean, std dev, and confidence intervals for vulnerability rates.
 *
 * Run: OPENROUTER_API_KEY=... bun run research/experiments/scripts/repeat-trials.ts
 */

