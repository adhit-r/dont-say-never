# ─── Result persistence ───────────────────────────────────────────────────────


def load_existing(model_id: str) -> list:
    path = OUT_DIR / f"{model_id}.json"
    if path.exists():
        try:
            return json.load(open(path))["results"]
        except Exception:
            return []
    return []


def save_results(model_id: str, model_name: str, results: list, status: str):
    summary: dict = {}
    for r in results:
        key = f"{r['repo']}/{r['prompt_id']}"
        summary.setdefault(key, {}).setdefault(r["condition"], {"vuln": 0, "total": 0, "errors": 0})
        summary[key][r["condition"]]["total"] += 1
        if r["vulnerable"]:
            summary[key][r["condition"]]["vuln"] += 1
        if r.get("error"):
            summary[key][r["condition"]]["errors"] += 1

    with open(OUT_DIR / f"{model_id}.json", "w") as f:
        json.dump(
            {
                "metadata": {
                    "run_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
                    "model_id": model_id,
                    "model_name": model_name,
                    "trials_per_cell": TRIALS,
                    "total_trials": len(results),
                    "conditions": ["control", "negative-framing", "positive-framing"],
                    "prompts": len(PROMPTS),
                    "status": status,
                },
                "summary": summary,
                "results": results,
            },
            f,
            indent=2,
        )


# ─── Per-model runners ────────────────────────────────────────────────────────


async def run_one_cell(call_fn, model_info, prompt_info, condition_id, framing_key, results: list):
    """Run TRIALS for a single (prompt, condition) cell, appending to results."""
    system = build_system_prompt(prompt_info["cwe"], framing_key)
    done = sum(
        1 for r in results
        if r["prompt_id"] == prompt_info["id"] and r["condition"] == condition_id
    )
    if done >= TRIALS:
        return
    print(f"  {prompt_info['id']:20s} {condition_id:20s} ", end="", flush=True)
    for trial in range(done, TRIALS):
        attempt = 0
        while attempt < MAX_RETRIES:
            try:
                text = await call_fn(system, prompt_info["prompt"])
                code = extract_code(text)
                vuln = prompt_info["detector"](code)
                results.append(
                    {
                        "model_id": model_info["id"],
                        "repo": prompt_info["repo"],
                        "prompt_id": prompt_info["id"],
                        "cwe": prompt_info["cwe"],
                        "condition": condition_id,
                        "trial": trial,
                        "vulnerable": vuln,
                        "code_length": len(code),
                        "code_preview": code[:200],
                    }
                )
                print("V" if vuln else "S", end="", flush=True)
                break
            except Exception as e:
                attempt += 1
                if attempt >= MAX_RETRIES:
                    results.append(
                        {
                            "model_id": model_info["id"],
                            "repo": prompt_info["repo"],
                            "prompt_id": prompt_info["id"],
                            "cwe": prompt_info["cwe"],
                            "condition": condition_id,
                            "trial": trial,
                            "vulnerable": False,
                            "code_length": 0,
                            "code_preview": "",
                            "error": str(e)[:200],
                        }
                    )
                    print("E", end="", flush=True)
                else:
                    await asyncio.sleep(5 * attempt)
        await asyncio.sleep(INTER_TRIAL_SEC)
        save_results(model_info["id"], model_info["name"], results, "in-progress")
    print()


async def run_model(model_info: dict, http_session: Optional[aiohttp.ClientSession] = None):
    """Run all prompts × conditions × trials for one model."""
    results = load_existing(model_info["id"])
    print(f"\n{'█' * 70}")
    print(f"MODEL: {model_info['name']} ({model_info['api']}) — resuming from {len(results)}/360 trials")
    print("█" * 70)

    api = model_info["api"]

    if api == "claude-cli":
        async def call_fn(system, prompt):
            # Offload sync subprocess call to threadpool
            return await asyncio.to_thread(call_claude_cli, model_info["alias"], system, prompt)
    elif api == "copilot":
        async def call_fn(system, prompt):
            return await call_copilot_sdk(model_info["copilot_id"], system, prompt)
    elif api == "gemini":
        api_key = os.environ["GEMINI_API_KEY"]
        async def call_fn(system, prompt):
            return await call_gemini(http_session, api_key, model_info["gemini_id"], system, prompt)
    elif api == "openrouter":
        api_key = os.environ["OPENROUTER_API_KEY"]
        async def call_fn(system, prompt):
            return await call_openrouter(http_session, api_key, model_info["or_id"], system, prompt)
    elif api == "openrouter-paid":
        api_key = os.environ["OPENROUTER_PAID_KEY"]
        async def call_fn(system, prompt):
            return await call_openrouter(http_session, api_key, model_info["or_id"], system, prompt)
    else:
        raise ValueError(api)

    for prompt_info in PROMPTS:
        for condition_id, framing_key in CONDITIONS:
            await run_one_cell(call_fn, model_info, prompt_info, condition_id, framing_key, results)
            await asyncio.sleep(INTER_CELL_SEC)

    save_results(model_info["id"], model_info["name"], results, "completed")
    print(f"\n✓ {model_info['name']} complete: {len(results)} trials")


