def build_system_prompt(cwe: str, framing: str) -> str:
    if framing == "control":
        return CONTROL_BASE
    t = TEMPLATES[cwe]
    title = t["neg_title"] if framing == "negative" else t["pos_title"]
    rule = t["neg_rule"] if framing == "negative" else t["pos_rule"]
    section = f"""<!-- PATCHPILOT:START -->
## Security rules — auto-generated by PatchPilot

### Stale AI patterns (insecure code from LLM training data)

- {t["severity"]} **{title}** (`{cwe}`)
  {rule}

<!-- PATCHPILOT:END -->"""
    return (
        f"{CONTROL_BASE}\n\n"
        "IMPORTANT — The following project rules were auto-generated by the security scanner. "
        "You MUST follow them:\n\n"
        f"{section}"
    )


