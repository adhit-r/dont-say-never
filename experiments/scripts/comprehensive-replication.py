def extract_code(text: str) -> str:
    blocks = re.findall(r"```(?:typescript|javascript|ts|js)?\n([\s\S]*?)```", text)
    return "\n".join(blocks) if blocks else text


# ─── Model callers ────────────────────────────────────────────────────────────


def call_claude_cli(model_alias: str, system: str, user_prompt: str) -> str:
    """Claude CLI via Max subscription. model_alias: 'sonnet', 'opus', or full model ID."""
    result = subprocess.run(
        [
            "claude", "-p",
            "--model", model_alias,
            "--output-format", "json",
            "--allowedTools", "",
            "--disallowedTools", "Write Edit Bash Read Glob Grep WebFetch WebSearch TodoWrite NotebookEdit",
            "--append-system-prompt", system,
            user_prompt,
        ],
        capture_output=True, text=True, timeout=180,
    )
    if result.returncode != 0:
        raise RuntimeError(f"Claude CLI error: {result.stderr[:200]}")
    try:
        return json.loads(result.stdout).get("result", "")
    except Exception:
        return result.stdout


async def call_copilot_sdk(model: str, system: str, user_prompt: str) -> str:
    """GitHub Copilot SDK."""
    from copilot import CopilotClient

    async def on_perm(req):
        return True

    full_prompt = f"System: {system}\n\nUser: {user_prompt}"
    async with CopilotClient() as client:
        async with await client.create_session(model=model, on_permission_request=on_perm) as session:
            done = asyncio.Event()
            parts: list[str] = []

            def on_event(event):
                if event.type.value == "assistant.message":
                    try:
                        parts.append(event.data.content)
                    except Exception:
                        parts.append(str(event.data))
                elif event.type.value == "session.idle":
                    done.set()

            session.on(on_event)
            await session.send(full_prompt)
            try:
                await asyncio.wait_for(done.wait(), timeout=180)
            except asyncio.TimeoutError:
                pass
            return "".join(parts)


async def call_gemini(session: aiohttp.ClientSession, api_key: str, model_id: str, system: str, user_prompt: str) -> str:
    url = f"https://generativelanguage.googleapis.com/v1beta/models/{model_id}:generateContent?key={api_key}"
    body = {
        "system_instruction": {"parts": [{"text": system}]},
        "contents": [{"role": "user", "parts": [{"text": user_prompt}]}],
    }
    async with session.post(url, json=body, timeout=aiohttp.ClientTimeout(total=120)) as resp:
        if resp.status != 200:
            text = await resp.text()
            raise RuntimeError(f"Gemini {resp.status}: {text[:200]}")
        data = await resp.json()
        cands = data.get("candidates", [])
        if not cands:
            return ""
        parts = cands[0].get("content", {}).get("parts", [])
        return parts[0].get("text", "") if parts else ""


async def call_openrouter(session: aiohttp.ClientSession, api_key: str, model_id: str, system: str, user_prompt: str) -> str:
    async with session.post(
        "https://openrouter.ai/api/v1/chat/completions",
        headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
        json={
            "model": model_id,
            "messages": [
                {"role": "system", "content": system},
                {"role": "user", "content": user_prompt},
            ],
        },
        timeout=aiohttp.ClientTimeout(total=120),
    ) as resp:
        if resp.status != 200:
            text = await resp.text()
            raise RuntimeError(f"OpenRouter {resp.status}: {text[:200]}")
        data = await resp.json()
        return data.get("choices", [{}])[0].get("message", {}).get("content", "")


