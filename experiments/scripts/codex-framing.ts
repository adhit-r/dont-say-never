// ─── Codex CLI call ───────────────────────────────────────────────────────────

function sleep(ms: number) {
  return new Promise((r) => setTimeout(r, ms));
}

function callCodex(workDir: string, prompt: string, outputFile: string): string {
  execFileSync("codex", [
    "exec",
    "-m", MODEL,
    "-C", workDir,
    "--ephemeral",
    "--sandbox", "read-only",
    "--skip-git-repo-check",
    // Disable MCP servers to reduce per-call latency
    "-c", "mcp_servers={}",
    "-o", outputFile,
    prompt,
  ], {
    timeout: 180_000,
    maxBuffer: 10 * 1024 * 1024,
    encoding: "utf8",
    stdio: "pipe",
  });
  return fs.existsSync(outputFile) ? fs.readFileSync(outputFile, "utf8") : "";
}

function extractCode(response: string): string {
  const blocks = response.match(/```(?:typescript|javascript|ts|js)?\n([\s\S]*?)```/g);
  if (blocks) {
    return blocks
      .map((b) => b.replace(/```(?:\w+)?\n?/, "").replace(/```$/, ""))
      .join("\n");
  }
  return response;
}

// ─── Result types ──────────────────────────────────────────────────────────────

interface TrialResult {
  model_id: string;
  model_name: string;
  repo: string;
  prompt_id: string;
  cwe: string;
  label: string;
  condition: "control" | "negative-framing" | "positive-framing";
  trial: number;
  vulnerable: boolean;
  code_length: number;
  code_preview: string;
  raw_response_preview: string;
  error?: string;
}

function buildSummary(results: TrialResult[]) {
  const summary: Record<string, Record<string, { vuln: number; total: number; errors: number }>> = {};
  for (const r of results) {
    const key = `${r.repo}/${r.prompt_id}`;
    summary[key] ??= {};
    summary[key][r.condition] ??= { vuln: 0, total: 0, errors: 0 };
    summary[key][r.condition].total++;
    if (r.vulnerable) summary[key][r.condition].vuln++;
    if (r.error)      summary[key][r.condition].errors++;
  }
  return summary;
}

function saveResults(results: TrialResult[], status: string) {
  fs.writeFileSync(OUT_PATH, JSON.stringify({
    metadata: {
      run_at: new Date().toISOString(),
      model_id: MODEL,
      model_name: "GPT-5 (gpt-5.3-codex via Codex CLI)",
      trials_per_cell: TRIALS,
      total_trials: results.length,
      conditions: ["control", "negative-framing", "positive-framing"],
      prompts: PROMPTS.length,
      cwes: [...new Set(PROMPTS.map((p) => p.cwe))],
      rule_injection_method: "AGENTS.md in working directory (ecologically valid — Codex reads AGENTS.md)",
      claude_baseline: "See positive-framing-ablation-v2.json (Phase 2, 180 trials)",
      reference: "Wegner, D. M. (1994). Ironic processes of mental control. Psychological Review, 101(1), 34-52.",
      status,
    },
    summary: buildSummary(results),
    results,
  }, null, 2));
}

// ─── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  const totalTrials = PROMPTS.length * 3 * TRIALS;
  console.log("=".repeat(70));
  console.log("CODEX (GPT-5) FRAMING REPLICATION");
  console.log("Don't Say Never — Ironic Effects of Prohibition-Framed Security Rules");
  console.log("=".repeat(70));
  console.log(`Model:         ${MODEL} (GPT-5 via Codex CLI)`);
  console.log(`Prompts:       ${PROMPTS.length} (same as Phase 2)`);
  console.log(`Trials/cell:   ${TRIALS}`);
  console.log(`Total trials:  ${totalTrials}`);
  console.log(`Output:        ${OUT_PATH}`);
  console.log(`Rule method:   CLAUDE.md injection (working directory)\n`);

  // ── Set up shared working directory ────────────────────────────────────────
  const workDir = fs.mkdtempSync(path.join(os.tmpdir(), "codex-framing-"));
  const outputFile = path.join(workDir, "codex-out.txt");
  console.log(`Work dir: ${workDir}\n`);

  const allResults: TrialResult[] = [];

  try {
    for (const p of PROMPTS) {
      console.log(`\n${"━".repeat(70)}`);
      console.log(`${p.repo}/${p.id} (${p.cwe} — ${p.label})`);
      console.log("━".repeat(70));

      const conditions: Array<{ id: TrialResult["condition"]; framing: "control" | "negative" | "positive" }> = [
        { id: "control",          framing: "control" },
        { id: "negative-framing", framing: "negative" },
        { id: "positive-framing", framing: "positive" },
      ];

      for (const cond of conditions) {
        // Write AGENTS.md for this condition (Codex reads AGENTS.md, not CLAUDE.md)
        const agentsMd = buildAgentsMd(p.cwe, cond.framing);
        fs.writeFileSync(path.join(workDir, "AGENTS.md"), agentsMd);

        process.stdout.write(`  ${cond.id.padEnd(20)} `);

        for (let t = 0; t < TRIALS; t++) {
          // Remove stale output file
          if (fs.existsSync(outputFile)) fs.unlinkSync(outputFile);

          let attempt = 0;
          while (attempt < MAX_RETRIES) {
            try {
              const rawResp = callCodex(workDir, p.prompt, outputFile);
              const code = extractCode(rawResp);
              const vuln = p.isVulnerable(code);
              allResults.push({
                model_id: MODEL, model_name: "GPT-5 (gpt-5.3-codex)",
                repo: p.repo, prompt_id: p.id, cwe: p.cwe, label: p.label,
                condition: cond.id, trial: t,
                vulnerable: vuln,
                code_length: code.length,
                code_preview: code.slice(0, 200),
                raw_response_preview: rawResp.slice(0, 200),
              });
              process.stdout.write(vuln ? "V" : "S");
              break;
            } catch (e: any) {
              attempt++;
              if (attempt >= MAX_RETRIES) {
                allResults.push({
                  model_id: MODEL, model_name: "GPT-5 (gpt-5.3-codex)",
                  repo: p.repo, prompt_id: p.id, cwe: p.cwe, label: p.label,
                  condition: cond.id, trial: t,
                  vulnerable: false, code_length: 0,
                  code_preview: "", raw_response_preview: "",
                  error: e.message?.slice(0, 200),
                });
                process.stdout.write("E");
              } else {
                await sleep(8_000 * attempt);
              }
            }
          }

          await sleep(INTER_TRIAL_MS);
        }

        const vc = allResults.filter((r) => r.prompt_id === p.id && r.condition === cond.id && r.vulnerable).length;
        const ec = allResults.filter((r) => r.prompt_id === p.id && r.condition === cond.id && r.error).length;
        console.log(`  ${vc}/${TRIALS} vuln${ec > 0 ? `, ${ec} err` : ""}`);

        saveResults(allResults, "in-progress");
        await sleep(INTER_CONDITION_MS);
      }
    }
  } finally {
    // Clean up working directory
    fs.rmSync(workDir, { recursive: true, force: true });
  }

  saveResults(allResults, "completed");

  // ── Summary table ───────────────────────────────────────────────────────────
  console.log("\n" + "=".repeat(70));
  console.log(`SUMMARY — GPT-5 (${MODEL})`);
  console.log("=".repeat(70));
  console.log(`${"prompt".padEnd(25)} ${"CWE".padEnd(9)} ${"ctrl".padEnd(7)} ${"neg".padEnd(7)} ${"pos"}`);
  console.log("-".repeat(60));

  for (const p of PROMPTS) {
    const fmt = (cond: string) => {
      const r = allResults.filter((x) => x.prompt_id === p.id && x.condition === cond);
      const v = r.filter((x) => x.vulnerable).length;
      const e = r.filter((x) => x.error).length;
      return e > 0 ? `${v}/${r.length}(${e}E)` : `${v}/${r.length}`;
    };
    console.log(
      `${`${p.repo}/${p.id}`.padEnd(25)} ${p.cwe.padEnd(9)} ${fmt("control").padEnd(7)} ${fmt("negative-framing").padEnd(7)} ${fmt("positive-framing")}`
    );
  }

  const agg = (cond: string) => {
    const r = allResults.filter((x) => x.condition === cond && !x.error);
    return `${r.filter((x) => x.vulnerable).length}/${r.length}`;
  };
  console.log("-".repeat(60));
  console.log(`${"AGGREGATE".padEnd(25)} ${"".padEnd(9)} ${agg("control").padEnd(7)} ${agg("negative-framing").padEnd(7)} ${agg("positive-framing")}`);
  console.log(`\nSaved to: ${OUT_PATH}`);
}

main().catch((e) => {
  console.error("Codex framing replication failed:", e);
  process.exit(1);
});
